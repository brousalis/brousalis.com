$(document).ready(function () {

    function menu() {
      $(".menu-drop").hide();
      $(".menu").live("mouseover mouseout", function (e) {
          if (e.type == "mouseout") {
              $(this).find(".menu-drop").hide()
          } else {
              $(this).find(".menu-drop").show()
          }
      });
    }
    menu();

    function loginmenu() {
      var button= $('#login-button');
      var box = $('#login-box');
      var form = $('#login');
      button.removeAttr('href');
      button.mouseup(function(login) {
          box.toggle();
          button.toggleClass('active');
      });
      form.mouseup(function() { 
          return false;
      });
      $(this).mouseup(function(login) {
          if(!($(login.target).parent('#login-button').length > 0)) {
              button.removeClass('active');
              box.hide();
          }
      });
    }
    loginmenu();

    function forms() {
      $("input, select, textarea").focus(function() {
        if (this.value == this.defaultValue)
        {
          this.value = "";
          return false;
        }
      });
      $("input, textarea").blur(function() {
        if (this.value == "")
        {
          this.value = this.defaultValue;
          return false;
        }
      });
    }
    forms();

    function topbar() {
      $('#header-inner').css('top', '-300px');
      setTimeout(function() { 
        $('#header-inner').addClass('transition');
        $('#header-inner').css('top', '0px');
      }, 200);
    }
    topbar();
    
    function init_gallery() {
      $("#main").scrollable({
        vertical: true,
        keyboard: 'static',
        onSeek: function(event, i) {
          horizontal.eq(i).data("scrollable").focus();
        }
      });
       
      var horizontal = $(".scrollable").scrollable();
      horizontal.eq(0).data("scrollable").focus();
    }      
    init_gallery();

});
   
